package Unit2.Wrap;

class Wrapping {
    public static void main(String[] args) {
        int simpleInt = 5;
        double simpleDouble = 5.99;
        char simpleChar = 'A';
        Integer myInt = 5;
        Double myDouble = 5.99;
        Character myChar = 'A';
        System.out.println(myInt == simpleInt);
        System.out.println(myDouble == simpleDouble);
        System.out.println(myChar == simpleChar);
    }
}


